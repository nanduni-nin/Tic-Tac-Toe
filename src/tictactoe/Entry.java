    private void player1TxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_player1TxtKeyTyped
        char c = evt.getKeyChar();
        if (player1Txt.getText().length() < 70) {
            if (c == java.awt.event.KeyEvent.VK_ENTER) {
                player2Txt.requestFocus(true);
            }
        } else {
            evt.consume();
        }
    }//GEN-LAST:event_player1TxtKeyTyped

