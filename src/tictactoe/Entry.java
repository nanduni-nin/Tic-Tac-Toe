    private void startBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startBtnActionPerformed
        String player1 = "Computer";
        String player2 = "Computer";
        Game game;
        try {

            player1 = player1Txt.getText();
            player2 = player2Txt.getText();

            if ((player1.equals("") || player2.equals("")) && noOfPlayers == 2) {
                JOptionPane.showMessageDialog(null, "Please enter the names.", "Warning!", JOptionPane.OK_OPTION);
            } else if (player1.equals("") && noOfPlayers == 1) {
                JOptionPane.showMessageDialog(null, "Please enter the name.", "Warning!", JOptionPane.OK_OPTION);
            } else {
                game = new Game(player1, player2 , noOfPlayers);
                this.setVisible(false);                
            }

        } catch (IOException ex) {
            Logger.getLogger(Entry.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_startBtnActionPerformed

